package org.example;


public class Main {


    public static void main(String[] args) {

        int i = 10;

        if(i>10){
            System.out.println("Hello World!");
        }



        do{
            System.out.println("Hello World!");

        }
        while (i>10);


        while (i>10){
            System.out.println("Hello World!");
        }

        for (int j=0;j>10;j++){
            System.out.println("Hello World!");
        }


        int y = 5;
        int u = 6;


        System.out.println(y++);
        System.out.println(++u);



    }
}





